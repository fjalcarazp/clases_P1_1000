/************ Mouse events: ****************/

// click - se hace click sobre un elemento
// contextmenu - cuando se hace click derecho en un elemento
// mouseover / mouseout - cuando el ratón pasa por encima del elemento / cuando sale del elemento.
// mousedown / mouseup - cuando se pulsa el botón del ratón / cuando se deja de pulsar sobre un elemento
// mousemove - cuando se mueve el ratón


/********** Keyboard events: ************/
// keydown y keyup - cuando una tecla se pulsa y se suelta


/************ Form element events: **************/

// submit - cuando se envía un <form>.
// focus - cuando se hace focus sobre un elemento por ejemplo un <input>.

/********** Document events: ************/

// DOMContentLoaded - cuando el HTML termina de cargar


/******* CSS events: *********/
// transitionend - cuando una animación de CSS termina
